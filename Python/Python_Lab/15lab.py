#1 lab of Arithmetic operators
a=4
b=6

#Addition
add_=a+b
print("Addition {}+{}+{}".format(a,b,add_))

#subtraction

sub=a-b
print("sub: {}-{}={}".format(a,b,sub))

#multitiplication

mul=a*b
print("multiplication : {}*{}={}".format(a,b,mul))

div=a/b
print("divition : {}/{}={}".format(a,b,div))

fldiv=a//b
print("floor divition : {}//{}={}".format(a,b,fldiv))

mod=a**b
print("Modulus : {}%{}={}".format(a,b,mod))

exp=a*b
